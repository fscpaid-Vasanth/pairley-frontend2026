export const getDealsByMode = (mode) =>
  mockDeals.filter((deal) => deal.mode === mode);

export const getPairDeals = () => getDealsByMode('pair');
export const getGroupDeals = () => getDealsByMode('group');
export const getActiveDeals = () =>
  mockDeals.filter((deal) => deal.status === 'active');

export const getFeaturedDeals = () => mockDeals.slice(0, 6);

export const getCurrentTier = (deal) => {
  if (deal.mode !== 'group' || !deal.pricingTiers) return null;
  const tiers = [...deal.pricingTiers].sort((a, b) => b.minPeople - a.minPeople);
  return tiers.find((tier) => deal.interestCount >= tier.minPeople) || deal.pricingTiers[0];
};

export const getNextTier = (deal) => {
  if (deal.mode !== 'group' || !deal.pricingTiers) return null;
  const currentTier = getCurrentTier(deal);
  const currentIndex = deal.pricingTiers.indexOf(currentTier);
  return currentIndex < deal.pricingTiers.length - 1
    ? deal.pricingTiers[currentIndex + 1]
    : null;
};
